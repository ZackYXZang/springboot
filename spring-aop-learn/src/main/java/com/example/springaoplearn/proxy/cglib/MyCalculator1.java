package com.example.springaoplearn.proxy.cglib;

public class MyCalculator1 {

  public int add(int i, int j) {
    return i + j;
  }

  public int sub(int i, int j) {
    return i - j;
  }

  public int mult(int i, int j) {
    return i * j;
  }

  public int div(int i, int j) {
    return i / j;
  }
}
